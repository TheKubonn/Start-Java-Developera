public class SectionThree {
    public static void main(String[] args) {
        // An integer is a whole number, meaning it doesn't contain a fractional element, or a decimal
        // Every Primitive types got their minimal and maximum value

        int myMinIntValue = Integer.MIN_VALUE;
        System.out.println("Integer min value: " + myMinIntValue);

        // One way to display it
        int myMaxIntValue = Integer.MAX_VALUE;
        System.out.println("Integer max value: " + myMaxIntValue);

        // We don't need to assign it to any data type to display
        System.out.println("Integer max value: " + Integer.MAX_VALUE);

        System.out.println("Integer Value Range (" + Integer.MIN_VALUE + " to " + Integer.MAX_VALUE + ")" );

    }
}
